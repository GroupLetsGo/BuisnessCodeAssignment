class ExerciseThree {
    public static void main(String[] args){

        int max = 100;
        int i = 0;
        int min = 0;
        int j = 100;
        while (i != max){
            i++;   
        System.out.println(i);
        }
        while (j != min ){
            j--;
        System.out.println(j);
        }
    }
}
